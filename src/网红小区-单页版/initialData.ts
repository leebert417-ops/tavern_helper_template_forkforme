/**
 * 日程表类型定义
 * 每个时间段指定租客应该在哪个位置
 */
export interface ScheduleEntry {
  /** 开始时间，格式：HH:MM */
  startTime: string;
  /** 结束时间，格式：HH:MM */
  endTime: string;
  /** 该时间段应该在的位置（房间号） */
  location: string;
  /** 该时间段在做什么（可选，用于未来的叙事描述） */
  activity?: string;
}

/**
 * 租客数据接口
 */
export interface Tenant {
  年龄: number;
  外貌: string;
  职业: string;
  性格: string;
  恋情: string;
  内心: string;
  状态: string;
  穿搭: string;
  好感度: number;
  性欲: number;
  入住日期: string;
  入住天数: number;
  当前位置: string;
  创作内容: string;
  关系: Record<string, string>;
  /** 日程表：定义租客在一天中不同时间段的位置 */
  schedule: ScheduleEntry[];
}

